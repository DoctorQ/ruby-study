# 变量类型
# 常量，全大写,不会改变的值
MY_FIRST_CONSTANT = 'hELLO WORLD'
puts MY_FIRST_CONSTANT
# 全局变量，$符号开头
$global_var = "Global Hello world"
puts $global_var
# 类变量，两个@符号开头
@@class_var = "class hello world"
puts @@class_var
# 实例变量，一个@符号开头
@instance_var = "instance hello world "
puts @instance_var
# 本地变量，小写
local_var = "Local helloworld";
puts local_var
